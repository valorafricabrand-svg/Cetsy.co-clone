• Use search, filters, categories; save favorites and follow shops

2. Buy Smart
• Read descriptions carefully; examine photos; be mindful of color variance
• Check seller reviews and shop policies (shipping/returns/custom orders)
• Ask questions before buying via "Message Seller"

3. Payments & Shipping
• Use Cetsy secure checkout; choose protected payment methods
• Ensure accurate shipping address; use tracking

4. After Purchase
• Track your order; contact seller first for issues; open a case if unresolved
• Leave fair, honest reviews focused on the transaction

5. Safety
• Use strong passwords; enable 2FA; beware of phishing
• Report suspicious activity or off-platform requests
