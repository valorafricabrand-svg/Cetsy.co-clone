1. Information We Collect
We collect various types of information from and about you to provide and improve our
Services, facilitate transactions between buyers and sellers, and ensure a secure and
personalized experience.

1.1. Information You Provide Directly to Us
• Account Information: When you create an account, we collect your name, email address, password, and possibly your phone number. For sellers, we may collect additional business information, such as your store name, business address, and tax identification number.
• Profile Information: You may choose to provide additional information for your profile, such as a profile picture, biography, social media links, and preferences.
• Transaction Information: When you make a purchase or sale through our Services, we collect information related to the transaction, including payment details (e.g., credit card number, billing address, PayPal information – though we typically use third-party payment processors), shipping address, order details, and contact information of the buyer/seller.
• Communications: If you communicate with us directly, such as through customer support, emails, or chat, we collect the content of those communications and any information you provide.
• Content You Submit: This includes reviews, comments, forum posts, product listings (including descriptions, images, and pricing), and any other content you create or upload to the Services.
• Surveys and Promotions: If you participate in surveys, contests, or promotions, we collect the information you provide in response.

1.2. Information We Collect Automatically
• Usage Information: We collect information about your interactions with our Services, such as the pages or listings you view, the searches you conduct, the features you use, the time and date of your visits, and the referring URL.
• Device Information: We collect information from and about the devices you use to access our Services, including IP address, device type, operating system, browser type, unique device identifiers, mobile network information, and crash data.
• Location Information: We may collect approximate location information (e.g., from your IP address or device settings) to provide location-based services, such as showing products available in your region or tailoring search results. You can often control this through your device settings.
• Cookies and Similar Technologies: We use cookies, web beacons, and other tracking technologies to collect information about your browsing activity, remember your preferences, personalize your experience, analyze trends, administer the website, track users’ movements around the website, and gather demographic information about our user base. For more details, please refer to our Cookie Policy (which is part of this Privacy Policy).

1.3. Information from Third Parties
• Payment Processors: We work with third-party payment processors (e.g., Stripe, PayPal) to facilitate transactions. These processors collect and process your payment information directly and may share with us non-sensitive information about your transactions (e.g., transaction ID, amount, status).
• Social Media: If you log in or connect your Cetsy account through a third-party social media service (e.g., Facebook, Google), we may receive information from that service, such as your profile information, public name, email address, and friend list, depending on your privacy settings on that service.
• Analytics Providers: We receive data from third-party analytics providers (e.g., Google Analytics) that help us understand how users interact with our Services, which may include demographic data and browsing behavior.
• Marketing Partners: We may receive information from marketing partners, such as advertising networks, to help us measure the effectiveness of our advertising campaigns and to deliver more relevant ads to you.

