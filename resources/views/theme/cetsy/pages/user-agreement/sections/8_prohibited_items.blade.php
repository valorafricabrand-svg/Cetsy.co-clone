  <h2 class="fw-semibold mb-2">Cetsy Prohibited Items Policy</h2>
  <p class="text-muted">Effective June 2025</p>
  <pre class="p-3 bg-light border rounded" style="white-space: pre-wrap;">Zero-Tolerance
• No items promoting hatred or violence; no unlawful items

Enforcement & Agreement
• Opening a shop/placing ads implies agreement to policies; Cetsy may deactivate content or suspend/terminate accounts; notice generally provided except for severe cases

Categories (non-exhaustive)
• Animal products/human remains: no endangered species (per CITES Appendix I), no ivory; limited exceptions for human teeth/hair/nails when legal and hygienic
• Recalled items: prohibited
• Hate/violent items: prohibited; firearms/knives only for lawful purposes (hunting/sport/collecting), not to cause harm
• Illegal/highly regulated: illegal/stolen items prohibited; comply with licensing/age restrictions; regional restrictions may apply
• Counterfeits/unauthorized goods: prohibited, including upcycled items implying endorsement
• Personal information/government documents: prohibited unless sold by issuing authority
• Mature adult content/nudity: no pornography; no sexualization of minors; no non-consensual acts/bestiality
• Glorifying human tragedy/suffering: prohibited (historical wartime items allowed if not promoting hate/violence)
• Encouraging self-harm/misinformation: prohibited
• Concealed/banned weapons: prohibited

Reporting
• Report non-conforming items/users to Cetsy for review

Third-Party Processor Disclaimer
• External processors (PayPal, Stripe, etc.) may block transactions per their compliance

Updates
• Policy may change; continued use indicates acceptance
